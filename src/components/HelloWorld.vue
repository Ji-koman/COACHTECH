<template>
    <div id="app">
        <p>{{message}}</p>
        <input type="text" placeholder="郵便番号を入力" v-model="zipcode"/>
        <button @click="getAddress">住所自動入力</button>
        <p>Address: {{Address['Address']}}</p>
    </div>
</template>

<script>
const axios = require('axios');

let url = 'https://apis.postcode-jp.com/api/v4/postcodes/postcode?apikey=SHBb270y2vmluvQm8K81yQEsw3BnBtlwmsp8ClH'

export default {
    data() {
      return {
          zipcode: '',
          Address: {}
    }
  },
methods: {
    getAddress() {
       axios.get(url + this.zipcode).then((res) => {
         const data = res.data.results[0];
         this.Address = data.allAddress;
       })
    }
  }
}
</script>
